
public class Variable_Initiating {
	public static void main(String[] args) {
		long salary=50000;
		float number=3.55f;
		char letter='K';
		boolean bool=true;
		String text="Kaviya";
		long l=902490343;
		double dou=423993;
		short mark=99;
		int experience=5;
	   System.out.println("long data type: "+salary);
	   System.out.println("float data type: "+number);
	   System.out.println("char data type: "+letter);
	   System.out.println("boolean data type: "+bool);
	   System.out.println("String data type: "+text);
	   System.out.println("long data type: "+l);
	   System.out.println("double data type: "+dou);
	   System.out.println("short data type: "+mark);
	   System.out.println("int data type: "+experience);
	   
		
		
				
	}

}
